def inject_monochromatic(
    n_samples: int,
    fs_hz: float,
    rng: np.random.Generator,
    params: dict[str, float] | None = None,
) -> SignalSegment:
    """Generate a near-monochromatic sinusoid with small frequency drift.

    Parameters
    ----------
    n_samples : int
        Segment length in samples.
    fs_hz : float
        Sampling frequency in Hz.
    rng : np.random.Generator
        Used only when ``params`` is ``None``.
    params : dict[str, float] or None, optional
        Output of :func:`draw_monochromatic_params`. Defaults to a fresh draw.

    Returns
    -------
    SignalSegment
        Strain ``A cos(2 pi (f t + 0.5 fdot t^2) + phi)``.
    """
    p: dict[str, float] = (
        params if params is not None else draw_monochromatic_params(rng)
    )
    meta = InjectionMeta(kind="monochromatic", params=dict(p))

    t: npt.NDArray[np.float64] = np.arange(n_samples, dtype=np.float64) / fs_hz
    phase: npt.NDArray[np.float64] = (
        2.0 * np.pi * (p["f_hz"] * t + 0.5 * p["fdot_hz_per_s"] * t**2)
        + p["phase_rad"]
    )
    strain: npt.NDArray[np.float64] = p["amplitude"] * np.cos(phase)

    return SignalSegment(strain=strain, fs_hz=float(fs_hz), meta=meta)

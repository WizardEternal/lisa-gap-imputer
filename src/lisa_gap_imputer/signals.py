"""Injected gravitational-wave signals for the gap-imputation training set.

Two injection classes are supported:

1. **SMBHB chirp** — a post-Newtonian inspiral waveform generated by the upstream
   :mod:`smbhb_inspiral` package. The upstream integrator returns a trajectory on
   an internal log-spaced time grid; here we resample onto a user-supplied
   uniform cadence and place the merger at a chosen offset within the segment.

2. **Monochromatic galactic binary** — a single sinusoid at a fixed frequency
   with a small linear frequency drift, standing in for the many near-monochromatic
   detached white-dwarf binaries that dominate the LISA-band foreground.

In both cases the signal is returned on the same uniform time grid the noise
module uses, so the two can be added pointwise to form the clean "truth" strain
fed into the dataset.

**Important scope reminder**

This is not a realistic LISA injection catalogue. The SMBHB population is drawn
uniformly in ``(log M_tot, log q, log d_L, cos iota)`` from ranges that make the
chirp visible inside a short segment, not from the astrophysical mass function.
The monochromatic population is one sinusoid per segment, not the ~30 million
resolved and unresolved sources LISA will actually see.

References
----------
Maggiore, M. 2007, *Gravitational Waves: Theory and Experiments*, Oxford.
Peters, P. C. 1964, Phys. Rev. 136, B1224.
Blanchet, L. 2014, Living Rev. Relativity 17, 2.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Literal

import numpy as np
import numpy.typing as npt
from smbhb_inspiral.physics import f_isco, integrate_inspiral
from smbhb_inspiral.waveform import strain_plus

__all__ = [
    "InjectionMeta",
    "SignalSegment",
    "inject_smbhb_chirp",
    "inject_monochromatic",
    "draw_smbhb_params",
    "draw_monochromatic_params",
]


SignalType = Literal["smbhb", "monochromatic", "quiet"]


@dataclass(frozen=True)
class InjectionMeta:
    """Metadata describing the injection drawn for a training segment.

    Kept next to the waveform so the evaluation stage can stratify reconstruction
    quality and matched-filter SNR recovery by the injection parameters.

    Attributes
    ----------
    kind : SignalType
        Which injection type was used. ``"quiet"`` indicates noise only.
    params : dict[str, float]
        The physical parameter draw, keyed by parameter name. Empty for
        ``kind == "quiet"``.
    """

    kind: SignalType
    params: dict[str, float]


@dataclass(frozen=True)
class SignalSegment:
    """A clean (noise-free) signal strain on a uniform time grid."""

    strain: npt.NDArray[np.float64]
    fs_hz: float
    meta: InjectionMeta

    @property
    def n_samples(self) -> int:
        return int(self.strain.size)


# ---------------------------------------------------------------------------
# Parameter draws
# ---------------------------------------------------------------------------


def draw_smbhb_params(
    rng: np.random.Generator,
    m_total_range_msun: tuple[float, float] = (1.0e5, 1.0e7),
    q_range: tuple[float, float] = (0.1, 1.0),
    d_l_range_mpc: tuple[float, float] = (5.0e2, 1.0e4),
    iota_range_rad: tuple[float, float] = (0.0, np.pi),
) -> dict[str, float]:
    """Draw SMBHB parameters for a single injection.

    Samples are uniform in ``log10(M_tot)``, ``log10(q)``, ``log10(d_L)``, and
    ``cos(iota)`` over the requested ranges. These priors are deliberately
    coarse; they populate the mass / distance / orientation plane well enough
    to probe how reconstruction quality depends on chirp duration and amplitude.
    """
    log_m_lo, log_m_hi = np.log10(m_total_range_msun)
    m_total: float = 10.0 ** rng.uniform(log_m_lo, log_m_hi)

    log_q_lo, log_q_hi = np.log10(q_range)
    q: float = 10.0 ** rng.uniform(log_q_lo, log_q_hi)

    log_d_lo, log_d_hi = np.log10(d_l_range_mpc)
    d_l_mpc: float = 10.0 ** rng.uniform(log_d_lo, log_d_hi)

    cos_iota_lo: float = float(np.cos(iota_range_rad[1]))
    cos_iota_hi: float = float(np.cos(iota_range_rad[0]))
    cos_iota: float = rng.uniform(
        min(cos_iota_lo, cos_iota_hi), max(cos_iota_lo, cos_iota_hi)
    )
    iota: float = float(np.arccos(np.clip(cos_iota, -1.0, 1.0)))

    # Derived component masses from M_tot and q (q = m2/m1 ≤ 1).
    m1: float = m_total / (1.0 + q)
    m2: float = m_total - m1

    return {
        "m1_msun": m1,
        "m2_msun": m2,
        "m_total_msun": m_total,
        "q": q,
        "d_l_mpc": d_l_mpc,
        "iota_rad": iota,
    }


def draw_monochromatic_params(
    rng: np.random.Generator,
    f_range_hz: tuple[float, float] = (3.0e-4, 1.0e-2),
    amp_range: tuple[float, float] = (5.0e-22, 5.0e-20),
    fdot_range_hz_per_s: tuple[float, float] = (0.0, 1.0e-16),
) -> dict[str, float]:
    """Draw parameters for a single near-monochromatic galactic-binary source.

    The ranges above are loose stand-ins tuned to put the sinusoid in the
    LISA-sensitive mHz band and at amplitudes comparable to the Robson+19 PSD
    at those frequencies. They are not drawn from the LISA Verification Binary
    catalogue.
    """
    log_f_lo, log_f_hi = np.log10(f_range_hz)
    f_hz: float = 10.0 ** rng.uniform(log_f_lo, log_f_hi)

    log_a_lo, log_a_hi = np.log10(amp_range)
    amplitude: float = 10.0 ** rng.uniform(log_a_lo, log_a_hi)

    fdot: float = float(rng.uniform(*fdot_range_hz_per_s))

    phase: float = float(rng.uniform(0.0, 2.0 * np.pi))

    return {
        "f_hz": f_hz,
        "amplitude": amplitude,
        "fdot_hz_per_s": fdot,
        "phase_rad": phase,
    }



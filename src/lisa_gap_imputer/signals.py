# SMBHB chirp injection
# ---------------------------------------------------------------------------


def _pick_start_frequency(
    m_total_msun: float,
    segment_duration_s: float,
    target_fraction_in_band: float = 0.6,
) -> float:
    """Choose an initial GW frequency so the inspiral lasts long enough to fill
    a useful fraction of the segment before merger.

    The Peters (1964) 0PN merger time ``t_merge ~ (5/256) c^5 a_0^4 / (G^3 m1 m2 M)``
    is monotonic in ``f_0``, so we use a crude geometric guess: pick ``f_0`` such
    that the analytic ISCO frequency is ``1/target_fraction_in_band`` factors of
    10 above it, capped to the LISA band. A fixed-point refinement would be
    prettier but unnecessary here — the downstream resampler will trim whatever
    length we end up with.
    """
    f_isco_hz: float = f_isco(m_total_msun)
    # Heuristic: start a few e-folds below ISCO so the chirp sweeps across the
    # segment rather than sitting in one bin.
    # Concrete recipe — for M_tot = 1e6 Msun, f_isco ~ 4 mHz; starting at
    # ~1e-2 * f_isco sweeps ~2 orders of magnitude, visible in a 10-hour segment.
    f0: float = 1.0e-2 * f_isco_hz
    # Clamp to the LISA analytic-PSD range.
    return float(np.clip(f0, 1.0e-5, 0.5 * f_isco_hz))


def inject_smbhb_chirp(
    n_samples: int,
    fs_hz: float,
    rng: np.random.Generator,
    params: dict[str, float] | None = None,
    merger_position: float | None = None,
) -> SignalSegment:
    """Generate a plus-polarization SMBHB chirp on a uniform time grid.

    Parameters
    ----------
    n_samples : int
        Length of the output segment in samples.
    fs_hz : float
        Sampling frequency in Hz.
    rng : np.random.Generator
        Random generator used when ``params`` is ``None`` (to draw parameters)
        and when ``merger_position`` is ``None`` (to draw the merger offset).
    params : dict[str, float] or None, optional
        Output of :func:`draw_smbhb_params`. If ``None``, a fresh draw is made
        with the default prior ranges.
    merger_position : float or None, optional
        Fractional position of the merger inside the segment, in ``[0, 1]``.
        ``0.5`` centres the merger; ``1.0`` places it at the last sample. If
        ``None``, drawn uniformly in ``[0.3, 0.9]`` so the chirp is always
        at least partially visible.

    Returns
    -------
    SignalSegment
        The plus-polarization strain ``h_+(t)`` resampled onto the uniform
        grid, with zeros everywhere outside the integrated trajectory.

    Notes
    -----
    Only the plus polarization is used — the cross polarization is coherent
    but carries no additional information about the gap-imputation problem
    at the phenomenological level we are operating on here. If the ablation
    later shows this matters we will revisit.

    The upstream ``integrate_inspiral`` is deterministic in its own right; the
    ``rng`` argument only enters through the parameter draw and merger-offset
    draw. Passing a pre-built ``params`` dictionary together with a fixed
    ``merger_position`` gives fully reproducible output.
    """
    p: dict[str, float] = params if params is not None else draw_smbhb_params(rng)
    meta = InjectionMeta(kind="smbhb", params=dict(p))

    segment_duration_s: float = n_samples / fs_hz
    f0_hz: float = _pick_start_frequency(p["m_total_msun"], segment_duration_s)

    trajectory = integrate_inspiral(
        m1=p["m1_msun"],
        m2=p["m2_msun"],
        f0=f0_hz,
        pn_order=1,
    )

    h_plus_traj: npt.NDArray[np.float64] = strain_plus(
        trajectory=trajectory,
        d_l_mpc=p["d_l_mpc"],
        iota=p["iota_rad"],
    )

    # Uniform output time grid (seconds).
    t_out: npt.NDArray[np.float64] = np.arange(n_samples, dtype=np.float64) / fs_hz

    # Place the trajectory so its endpoint (merger) falls at the chosen
    # fractional position within the output segment.
    if merger_position is None:
        merger_position = float(rng.uniform(0.3, 0.9))
    merger_position = float(np.clip(merger_position, 0.0, 1.0))
    t_merger_out: float = merger_position * segment_duration_s

    # Shift trajectory so t_traj_end maps to t_merger_out.
    t_traj_end: float = float(trajectory.t[-1])
    t_traj_shifted: npt.NDArray[np.float64] = (
        trajectory.t - t_traj_end + t_merger_out
    )

    # Resample via linear interpolation. Points outside the trajectory return 0.
    h_plus_out: npt.NDArray[np.float64] = np.interp(
        t_out,
        t_traj_shifted,
        h_plus_traj,
        left=0.0,
        right=0.0,
    )

    return SignalSegment(strain=h_plus_out, fs_hz=float(fs_hz), meta=meta)


